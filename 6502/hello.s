
    .org $fffc
    .word reset
    .word 0

;;; VIA
PORTB = $6000                   ; LCD data
PORTA = $6001                   ; LCD control; using 3 bits
DDRB = $6002
DDRA = $6003

;;; LCD control bits on port A
E  = %10000000
RW = %01000000                  ; 1-read
RS = %00100000                  ; 1-data

    .org $8000

reset:
    jsr init_display
    jsr clear_display
    jsr send_message1
    jsr pause_3000              ; about 1s at my top clock speed of about 3kHz
    jsr clear_display
    jsr send_message2
spin:
    jmp spin

init_display:
    lda #$ff
    sta DDRB
    sta DDRA
    lda #%00111000              ; function set: 8 bit; 2 lines, 5x8
    jsr send_lcd_command
    lda #%00001100              ; turn on display; no cursor; no blinking
    jsr send_lcd_command
    lda #%00000110              ; entry mode: increment; no shift
    jsr send_lcd_command
    rts

clear_display:
    lda #%00000001
    jsr send_lcd_command
    rts

send_message1:
    lda #'H'
    jsr send_lcd_data
    lda #'e'
    jsr send_lcd_data
    lda #'l'
    jsr send_lcd_data
    lda #'l'
    jsr send_lcd_data
    lda #'o'
    jsr send_lcd_data
    lda #','
    jsr send_lcd_data
    lda #' '
    jsr send_lcd_data
    lda #'W'
    jsr send_lcd_data
    lda #'o'
    jsr send_lcd_data
    lda #'r'
    jsr send_lcd_data
    lda #'l'
    jsr send_lcd_data
    lda #'d'
    jsr send_lcd_data
    lda #'!'
    jsr send_lcd_data
    rts

send_message2:
    lda #'T'
    jsr send_lcd_data
    lda #'h'
    jsr send_lcd_data
    lda #'i'
    jsr send_lcd_data
    lda #'s'
    jsr send_lcd_data
    lda #' '
    jsr send_lcd_data
    lda #'i'
    jsr send_lcd_data
    lda #'s'
    jsr send_lcd_data
    lda #' '
    jsr send_lcd_data
    lda #'f'
    jsr send_lcd_data
    lda #'u'
    jsr send_lcd_data
    lda #'n'
    jsr send_lcd_data
    lda #'!'
    jsr send_lcd_data
    rts

send_lcd_command:
    sta PORTB
    lda #0
    sta PORTA
    lda #(E)
    sta PORTA
    lda #0
    sta PORTA
    rts

send_lcd_data:
    sta PORTB
    lda #(RS)
    sta PORTA
    lda #(RS | E)
    sta PORTA
    lda #(RS)
    sta PORTA
    rts

